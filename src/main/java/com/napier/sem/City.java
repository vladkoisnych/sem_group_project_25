package com.napier.sem;

/**
 * Represents an city
 */
public class City
{
    /**
     * City ID
     */
    public int city_id;

    /**
     *  City Name
     */
    public String city_name;

    /**
     * City`s County Code
     */
    public String city_county_code;

    /**
     * City District
     */
    public String city_district;

    /**
     * City population
     */
    public int city_population;
}