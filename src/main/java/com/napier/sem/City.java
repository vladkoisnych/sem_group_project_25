package com.napier.sem;

/**
 * Represents a city
 */
public class City
{
    /**
     * City ID
     */
    public int city_id;

    /**
     *  City Name
     */
    public String city_name;

    /**
     * City`s Country Code
     */
    public String city_countryCode;

    /**
     * City District
     */
    public String city_district;

    /**
     * City population
     */
    public int city_population;
}